// Ritorna 1 se c è una lettera (a-z o A-Z), 0 altrimenti
#include "libft.h"

int	ft_isalpha(int c)
{
	if ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'))
		return (1);
	return (0);
}
